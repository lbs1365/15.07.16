package model;

public class InMet {
	private int imtNo;
	private String imtName;
	private int memNo;
	
	public int getImtNo() {
		return imtNo;
	}
	public void setImtNo(int imtNo) {
		this.imtNo = imtNo;
	}
	public String getImtName() {
		return imtName;
	}
	public void setImtName(String imtName) {
		this.imtName = imtName;
	}
	public int getMemNo() {
		return memNo;
	}
	public void setMemNo(int memNo) {
		this.memNo = memNo;
	}
	
}

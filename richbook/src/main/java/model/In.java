package model;

import java.util.Date;

public class In {
	private int inNo;
	private int imtNo;
	private Date inDate;
	private int inSum;
	private String inCon;
	private String inEtc;
	private int memNo;
	
	public int getInNo() {
		return inNo;
	}
	public void setInNo(int inNo) {
		this.inNo = inNo;
	}
	public int getImtNo() {
		return imtNo;
	}
	public void setImtNo(int imtNo) {
		this.imtNo = imtNo;
	}
	public Date getInDate() {
		return inDate;
	}
	public void setInDate(Date inDate) {
		this.inDate = inDate;
	}
	public int getInSum() {
		return inSum;
	}
	public void setInSum(int inSum) {
		this.inSum = inSum;
	}
	public String getInCon() {
		return inCon;
	}
	public void setInCon(String inCon) {
		this.inCon = inCon;
	}
	public String getInEtc() {
		return inEtc;
	}
	public void setInEtc(String inEtc) {
		this.inEtc = inEtc;
	}
	public int getMemNo() {
		return memNo;
	}
	public void setMemNo(int memNo) {
		this.memNo = memNo;
	}	
	
}
